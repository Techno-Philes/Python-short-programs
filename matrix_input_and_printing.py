b=int(input("colums: "))
matrix=[]
for i in range(a):#loop for rows
    c=[]
    for j in range(b):
        j=int(input("input "))
        c.append(j)
    print()
    matrix.append(c)
#printing
for i in range(a):
    for j in range(b):
        print(matrix[i][j],end=" ")
    print()

